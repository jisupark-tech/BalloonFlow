        /// <summary>
        /// 폴더 안의 모든 asset 을 그룹/라벨에 등록. KEEP_IN_RESOURCES 항목은 skip.
        /// Address(key) = constPrefix + filename (예: "popup_PopupResult", "audio_Stage_Clear")
        /// </summary>
        private static void MigrateFolder(string folder, string groupName, string label, string addressPrefix)
        {
            var settings = AddressableAssetSettingsDefaultObject.Settings;
            if (settings == null)
            {
                Debug.LogError($"{LOG_TAG} AddressableAssetSettings 없음. Setup Groups & Labels 먼저.");
                return;
            }
            var group = settings.FindGroup(groupName);
            if (group == null)
            {
                Debug.LogError($"{LOG_TAG} Group '{groupName}' 없음. Setup Groups & Labels 먼저.");
                return;
            }
            if (!settings.GetLabels().Contains(label)) settings.AddLabel(label);

            if (!System.IO.Directory.Exists(folder))
            {
                Debug.LogWarning($"{LOG_TAG} 폴더 없음: {folder}");
                return;
            }

            // 모든 asset (prefab + audio + 기타) — sprite/texture/material 은 의존성으로 자동 포함되므로 제외 권장
            var guids = AssetDatabase.FindAssets("", new[] { folder });
            int added = 0, skipped = 0;
            foreach (var guid in guids)
            {
                string path = AssetDatabase.GUIDToAssetPath(guid);
                if (AssetDatabase.IsValidFolder(path)) continue;

                string fileName = Path.GetFileNameWithoutExtension(path);
                if (string.IsNullOrEmpty(fileName)) continue;

                // KEEP 항목 skip (Resources/ 잔존)
                if (KEEP_IN_RESOURCES.Contains(fileName))
                {
                    skipped++;
                    continue;
                }

                // 등록 대상 type 제한 — Prefab / AudioClip / SpriteAtlas / Texture(필요시) / Material
                var assetType = AssetDatabase.GetMainAssetTypeAtPath(path);
                if (assetType == null) continue;
                if (assetType == typeof(MonoScript) || assetType.Name == "DefaultAsset") continue;

                var entry = settings.CreateOrMoveEntry(guid, group, readOnly: false, postEvent: false);
                if (entry == null) continue;
                entry.SetAddress(addressPrefix + fileName, postEvent: false);
                if (!entry.labels.Contains(label)) entry.SetLabel(label, true, true);
                added++;
            }

            EditorUtility.SetDirty(settings);
            AssetDatabase.SaveAssets();
            Debug.Log($"{LOG_TAG} '{folder}' → {groupName}/{label}: 등록 {added}, KEEP-skip {skipped}");
        }

        [MenuItem("BalloonFlow/Addressables/Auto-Migrate Sprite Atlases")]
        public static void AutoMigrateAtlases()
        {
            var settings = AddressableAssetSettingsDefaultObject.Settings;
            if (settings == null)
            {
                Debug.LogError($"{LOG_TAG} AddressableAssetSettings 없음. 'Setup Groups & Labels' 먼저 실행.");
                return;
            }

            var group = settings.FindGroup(GROUP_LOCAL_DEMAND);
            if (group == null)
            {
                Debug.LogError($"{LOG_TAG} Group '{GROUP_LOCAL_DEMAND}' 없음. 'Setup Groups & Labels' 먼저 실행.");
                return;
            }

            if (!settings.GetLabels().Contains(LABEL_UI)) settings.AddLabel(LABEL_UI);

            // Atlas 들 찾기
            var atlasGuids = AssetDatabase.FindAssets("t:SpriteAtlas", new[] { ATLAS_FOLDER });
            if (atlasGuids.Length == 0)
            {
                Debug.LogWarning($"{LOG_TAG} {ATLAS_FOLDER} 에 SpriteAtlas 없음.");
                return;
            }

            // Editor 에서 atlas 들이 아직 pack 안 됐을 수 있음 → 강제 pack (build 시 수행되는 것과 동등)
            var atlasObjs = atlasGuids
                .Select(g => AssetDatabase.LoadAssetAtPath<SpriteAtlas>(AssetDatabase.GUIDToAssetPath(g)))
                .Where(a => a != null)
                .ToArray();
            if (atlasObjs.Length > 0)
            {
                SpriteAtlasUtility.PackAtlases(atlasObjs, EditorUserBuildSettings.activeBuildTarget, canCancel: false);
            }

            // 등록 + sprite name 수집
            var atlasInfos = new List<AtlasInfo>();
            foreach (var guid in atlasGuids)
            {
                string path = AssetDatabase.GUIDToAssetPath(guid);
                string fileName = Path.GetFileNameWithoutExtension(path);
                string addr = "atlas_" + fileName.ToLowerInvariant();

                // Addressable 등록
                var entry = settings.CreateOrMoveEntry(guid, group, readOnly: false, postEvent: false);
                if (entry == null)
                {
                    Debug.LogWarning($"{LOG_TAG} entry 생성 실패: {path}");
                    continue;
                }
                entry.SetAddress(addr, postEvent: false);
                if (!entry.labels.Contains(LABEL_UI)) entry.SetLabel(LABEL_UI, true, true);

                // sprite name 수집 — pack 후 GetSprites 로 정확한 packed sprite 들. 그래도 0 이면 Packables 에서 fallback.
                var atlas = AssetDatabase.LoadAssetAtPath<SpriteAtlas>(path);
                if (atlas == null)
                {
                    Debug.LogWarning($"{LOG_TAG} atlas 로드 실패: {path}");
                    continue;
                }

                var spriteNames = new HashSet<string>();
                int packedCount = atlas.spriteCount;
                if (packedCount > 0)
                {
                    var sprites = new Sprite[packedCount];
                    atlas.GetSprites(sprites);
                    foreach (var s in sprites)
                        if (s != null) spriteNames.Add(StripCloneSuffix(s.name));
                }
                else
                {
                    // Pack 실패 / 빈 atlas → Packables (Object[] = 폴더/sprite/texture) 직접 탐색
                    var packables = atlas.GetPackables();
                    foreach (var p in packables)
                    {
                        if (p == null) continue;
                        string pPath = AssetDatabase.GetAssetPath(p);
                        if (string.IsNullOrEmpty(pPath)) continue;

                        if (AssetDatabase.IsValidFolder(pPath))
                        {
                            // 폴더 내부 모든 sprite
                            var sGuids = AssetDatabase.FindAssets("t:Sprite", new[] { pPath });
                            foreach (var sg in sGuids)
                            {
                                string sp = AssetDatabase.GUIDToAssetPath(sg);
                                var sub = AssetDatabase.LoadAllAssetsAtPath(sp);
                                foreach (var o in sub)
                                    if (o is Sprite spr) spriteNames.Add(StripCloneSuffix(spr.name));
                            }
                        }
                        else
                        {
                            var sub = AssetDatabase.LoadAllAssetsAtPath(pPath);
                            foreach (var o in sub)
                                if (o is Sprite spr) spriteNames.Add(StripCloneSuffix(spr.name));
                        }
                    }
                }

                var spriteList = spriteNames.OrderBy(n => n).ToList();
                atlasInfos.Add(new AtlasInfo
                {
                    atlasName = fileName,
                    addressableKey = addr,
                    spriteNames = spriteList
                });

                Debug.Log($"{LOG_TAG} '{fileName}.spriteatlas' 등록 — addr={addr}, sprites={spriteList.Count} (packed={packedCount})");
            }

            EditorUtility.SetDirty(settings);
            AssetDatabase.SaveAssets();

            // Const.cs 갱신
            UpdateConstFile(atlasInfos);
        }

        private static void UpdateConstFile(List<AtlasInfo> atlases)
        {
            string fullPath = Path.GetFullPath(CONST_FILE);
            if (!File.Exists(fullPath))
            {
                Debug.LogError($"{LOG_TAG} Const.cs 없음: {fullPath}");
                return;
            }

            var sb = new StringBuilder();
            sb.AppendLine(AUTOGEN_BEGIN);
            sb.AppendLine($"        // generated from {atlases.Count} SpriteAtlas(es) in {ATLAS_FOLDER}");
            sb.AppendLine();

            // Atlas key 들
            sb.AppendLine("        // SpriteAtlas Addressable keys");
            foreach (var a in atlases)
            {
                string constName = "ADDR_ATLAS_" + a.atlasName.ToUpperInvariant();
                sb.AppendLine($"        public const string {constName,-40} = \"{a.addressableKey}\";");
            }
            sb.AppendLine();

            // Sprite name 들 (atlas 별)
            foreach (var a in atlases)
            {
                sb.AppendLine($"        // Sprite names in '{a.atlasName}' atlas");
                foreach (var name in a.spriteNames)
                {
                    string constName = "SPR_" + ToConstSuffix(name);
                    sb.AppendLine($"        public const string {constName,-40} = \"{name}\";");
                }
                sb.AppendLine();
            }

            sb.AppendLine("        // 사용 예:");
            sb.AppendLine("        //   var spr = await AddressableSystem.GetSpriteAsync(Const.ADDR_ATLAS_UI, Const.SPR_ICONHAND);");
            sb.AppendLine();
            sb.AppendLine(AUTOGEN_END);

            string content = File.ReadAllText(fullPath);
            int regionStart = content.IndexOf(KEYS_REGION_BEGIN);
            if (regionStart < 0)
            {
                Debug.LogError($"{LOG_TAG} Const.cs 에 region 마커 없음: {KEYS_REGION_BEGIN}");
                return;
            }
            int regionEnd = content.IndexOf(KEYS_REGION_END, regionStart);
            if (regionEnd < 0)
            {
                Debug.LogError($"{LOG_TAG} region 종료 마커 없음.");
                return;
            }

            int contentStart = content.IndexOf('\n', regionStart) + 1;
            string before = content.Substring(0, contentStart);
            string after = content.Substring(regionEnd);

            string updated = before + sb.ToString() + "\n" + after;
            File.WriteAllText(fullPath, updated);
            AssetDatabase.Refresh();

            int totalSprites = atlases.Sum(a => a.spriteNames.Count);
            Debug.Log($"{LOG_TAG} ✔ Const.cs 갱신 — {atlases.Count} atlas + {totalSprites} sprite key 생성. " +
                      $"코드에서 'Const.SPR_ICONHAND' 처럼 사용.");
        }

        /// <summary>"iconHand(Clone)" → "iconHand"</summary>
        private static string StripCloneSuffix(string name)
        {
            int p = name.IndexOf('(');
            return p > 0 ? name.Substring(0, p).TrimEnd() : name;
        }

        /// <summary>"iconHand" → "ICONHAND", "iconHaptic_off" → "ICONHAPTIC_OFF"
        /// (단순 대문자화. snake_case 변환 시 충돌/혼동 방지 위해 그대로.)</summary>
        private static string ToConstSuffix(string name)
        {
            // 영숫자 + 언더스코어만 남기기
            var sb = new StringBuilder(name.Length);
            foreach (var c in name)
            {
                if (char.IsLetterOrDigit(c) || c == '_') sb.Append(char.ToUpperInvariant(c));
                else sb.Append('_');
            }
            return sb.ToString();
        }

        private struct AtlasInfo
        {
            public string atlasName;       // "UI"
            public string addressableKey;  // "atlas_ui"
            public List<string> spriteNames;
        }
    }
