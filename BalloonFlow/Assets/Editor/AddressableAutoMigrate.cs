    /// <summary>
    /// SpriteAtlas 들을 Addressable 그룹에 일괄 등록 + atlas 내부 sprite name 들을 Const.cs 에 자동 생성.
    ///
    /// 메뉴:
    ///   BalloonFlow > Addressables > Auto-Migrate Sprite Atlases
    ///
    /// 동작:
    ///   1) Assets/4.Atlas/*.spriteatlas 모두 Local_OnDemand 그룹 + "ui" 라벨 등록.
    ///      Address(key) = "atlas_<name>" (예: UI.spriteatlas → atlas_ui)
    ///   2) 각 atlas 의 sprite name 들 enumerate → Const.cs 에:
    ///      - ADDR_ATLAS_UI = "atlas_ui"
    ///      - SPR_ICON_HAND = "iconHand"  (sprite name 그대로)
    ///   3) 런타임 사용:
    ///      var sprite = await AddressableSystem.GetSpriteAsync(Const.ADDR_ATLAS_UI, Const.SPR_ICON_HAND);
    /// </summary>
    public static class AddressableAutoMigrate
    {
        private const string LOG_TAG = "[AddrMigrate]";
        // 그룹명은 AddressableSetupTool 의 상수와 일치
        private const string GROUP_LOCAL_ALWAYS = "Local_Always";
        private const string GROUP_LOCAL_DEMAND = "Local_OnDemand";
        private const string GROUP_REMOTE_CDM   = "Remote_CDM";
        private const string LABEL_UI = "ui";
        private const string ATLAS_FOLDER = "Assets/4.Atlas";
        private const string CONST_FILE = "Assets/1.Scripts/Core/Const.cs";

        private const string KEYS_REGION_BEGIN = "        #region Addressable Keys — TODO: 마이그레이션 진행하며 채워넣기";
        private const string KEYS_REGION_END = "        #endregion";

        private const string AUTOGEN_BEGIN = "        // ── AUTO-GENERATED by AddressableAutoMigrate (do not edit manually) ──";
        private const string AUTOGEN_END   = "        // ── /AUTO-GENERATED ──";

        [MenuItem("BalloonFlow/Addressables/Migrate All (Popup + UI + Prefabs + Sound + Material + Atlas)")]
        public static void MigrateAll()
        {
            AutoMigrateAtlases();
            MigratePopups();
            MigrateUI();
            MigrateInGamePrefabs();
            MigrateAudio();
            MigrateMaterials();
            RegenerateConstFromAllGroups();
            UnityEditor.EditorUtility.DisplayDialog("Addressables Migration",
                "전체 마이그레이션 완료. Const.cs 자동 갱신됨.\n" +
                "그룹: Local_Always (core), Local_OnDemand (ui), Remote_CDM (cdm)\n\n" +
                "예외 (Resources/ 잔존): UITitle.prefab, PopupError.prefab", "OK");
        }

        /// <summary>모든 BalloonFlow 그룹의 entry 를 Const.cs 자동 영역에 합쳐 기록.</summary>
        [MenuItem("BalloonFlow/Addressables/Regenerate Const Keys")]
        public static void RegenerateConstFromAllGroups()
        {
            var settings = AddressableAssetSettingsDefaultObject.Settings;
            if (settings == null) return;

            var sb = new StringBuilder();
            sb.AppendLine(AUTOGEN_BEGIN);
            sb.AppendLine("        // generated from Addressable groups Local_Always / Local_OnDemand / Remote_CDM");
            sb.AppendLine();

            // SpriteAtlas 영역은 별도로 한 번 더 (atlas 과 sprite name 구분)
            sb.AppendLine("        // ── Atlas keys ──────────────────────────────────");
            EmitGroupEntries(sb, settings, GROUP_LOCAL_DEMAND, e => e.address.StartsWith("atlas_"), "ADDR_");
            sb.AppendLine();

            sb.AppendLine("        // ── Atlas sprite names (UI atlas) ───────────────");
            // sprite 이름들은 atlas 등록 시 enumerate 했지만 Const 기록이 분산되므로 재실행 가독성 위해 atlas 별 enumerate
            EmitAtlasSpriteNames(sb, settings, GROUP_LOCAL_DEMAND);
            sb.AppendLine();

            sb.AppendLine("        // ── Popup keys ──────────────────────────────────");
            EmitGroupEntries(sb, settings, GROUP_LOCAL_DEMAND, e => e.address.StartsWith("popup_"), "ADDR_");
            sb.AppendLine();

            sb.AppendLine("        // ── UI keys ─────────────────────────────────────");
            EmitGroupEntries(sb, settings, GROUP_LOCAL_DEMAND, e => e.address.StartsWith("ui_"), "ADDR_");
            sb.AppendLine();

            sb.AppendLine("        // ── InGame Prefab keys (Local_Always / core) ────");
            EmitGroupEntries(sb, settings, GROUP_LOCAL_ALWAYS, e => e.address.StartsWith("prefab_"), "ADDR_");
            sb.AppendLine();

            sb.AppendLine("        // ── Audio keys (Remote_CDM / cdm) ───────────────");
            EmitGroupEntries(sb, settings, GROUP_REMOTE_CDM, e => e.address.StartsWith("audio_"), "ADDR_");
            sb.AppendLine();

            sb.AppendLine("        // 사용 예:");
            sb.AppendLine("        //   var prefab = await AddressableSystem.LoadAssetAsync<GameObject>(Const.ADDR_POPUP_PopupResult);");
            sb.AppendLine("        //   var clip   = await AddressableSystem.LoadAssetAsync<AudioClip>(Const.ADDR_AUDIO_Stage_Clear);");
            sb.AppendLine();
            sb.AppendLine(AUTOGEN_END);

            WriteConstBlock(sb.ToString());
            Debug.Log($"{LOG_TAG} ✔ Const.cs 통합 갱신 완료.");
        }

        private static void EmitGroupEntries(StringBuilder sb, AddressableAssetSettings settings,
            string groupName, System.Func<AddressableAssetEntry, bool> filter, string constPrefix)
        {
            var group = settings.FindGroup(groupName);
            if (group == null) return;
            var entries = group.entries.Where(filter).OrderBy(e => e.address).ToList();
            foreach (var e in entries)
            {
                string suffix = ToConstSuffix(e.address);
                sb.AppendLine($"        public const string {constPrefix}{suffix,-40} = \"{e.address}\";");
            }
        }

        private static void EmitAtlasSpriteNames(StringBuilder sb, AddressableAssetSettings settings, string groupName)
        {
            var group = settings.FindGroup(groupName);
            if (group == null) return;
            var atlasEntries = group.entries.Where(e => e.address.StartsWith("atlas_")).ToList();
            foreach (var entry in atlasEntries)
            {
                var atlas = AssetDatabase.LoadAssetAtPath<SpriteAtlas>(entry.AssetPath);
                if (atlas == null) continue;
                int count = atlas.spriteCount;
                if (count <= 0) continue;
                var sprites = new Sprite[count];
                atlas.GetSprites(sprites);
                var names = sprites.Where(s => s != null).Select(s => StripCloneSuffix(s.name)).Distinct().OrderBy(n => n);
                foreach (var name in names)
                {
                    string suffix = ToConstSuffix(name);
                    sb.AppendLine($"        public const string SPR_{suffix,-40} = \"{name}\";");
                }
            }
        }

        private static void WriteConstBlock(string newBlock)
        {
            string fullPath = Path.GetFullPath(CONST_FILE);
            if (!File.Exists(fullPath)) return;
            string content = File.ReadAllText(fullPath);

            int regionStart = content.IndexOf(KEYS_REGION_BEGIN);
            if (regionStart < 0) return;
            int regionEnd = content.IndexOf(KEYS_REGION_END, regionStart);
            if (regionEnd < 0) return;

            int contentStart = content.IndexOf('\n', regionStart) + 1;
            string before = content.Substring(0, contentStart);
            string after = content.Substring(regionEnd);
            File.WriteAllText(fullPath, before + newBlock + "\n" + after);
            AssetDatabase.Refresh();
        }

